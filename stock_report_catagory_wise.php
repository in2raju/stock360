// Logged-in user info
$user    = $_SESSION['user'];
$orgCode = $user['org_code'];
$brCode  = $user['br_code'];   // âœ… BRANCH CODE ADDED

// Fetch organization details
$orgStmt = $pdo->prepare("
    SELECT ORGANIZATION_NAME, ORGANIZATION_ADDRESS 
    FROM organization_info 
    WHERE ORG_CODE = :org_code
");
$orgStmt->execute(['org_code' => $orgCode]);
$org = $orgStmt->fetch(PDO::FETCH_ASSOC);

$orgName    = $org['ORGANIZATION_NAME'] ?? 'Organization';
$orgAddress = $org['ORGANIZATION_ADDRESS'] ?? '';

// Fetch stock report (ORG + BRANCH wise)
$stockStmt = $pdo->prepare("
    SELECT 
        product_category_name, 
        supplier_name,
        SUM(remaining) AS total_quantity
    FROM stock_details_view
    WHERE org_code = :org_code
      AND br_code  = :br_code
    GROUP BY product_category_name, supplier_name
    ORDER BY product_category_name, supplier_name
");

$stockStmt->execute([
    'org_code' => $orgCode,
    'br_code'  => $brCode
]);

$stockData = $stockStmt->fetchAll(PDO::FETCH_ASSOC);

